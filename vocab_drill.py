class vocab_drill(object):
    def __init__(self, lable, tests):
        self.vocab_name = lable
        self.vocab_array = tests

    def run_drill(self, test_count):
        continue_test = True
        response = "Continue"
        if test_count == 0:
            test_count = len(self.vocab_array)
        v_choices = random.sample(self.vocab_array, test_count)
        index = 0

        print("\n*** STARTING *** %s" % self.vocab_name)
        while response != "Stop" and index < len(v_choices):
            try:
                print("\nTest: %s" % self.vocab_name)
                print("Special chars: \u00E0 \u00E1 \u00E2 \u00E7 \u00E8 \u00E9 \u00EA \u00EC \u00ED \u00EE \u00F2 \u00F3 \u00F4 \u00f9 \u00fa \u00fb")
                v_choice = v_choices[index]
                index = index + 1
                response = input(("French for '%s': " % v_choice[0]))
                if (response == "Stop"):
                    continue_test = False
                    break;
                elif (response == v_choice[1]):
                    print ("Correct!")
                else:
                    print ("Answer for '%s': %s" % (v_choice[0], v_choice[1]))
                    v_choices.append(v_choice)
            except KeyboardInterrupt as KI:
                print("\nExiting...")
                continue_test = False
                break
            except EOFError as EOFE:
                print("\nExiting...")
                continue_test = False
                break
        if continue_test:
            print("\n*** FINISHED *** %s" % self.vocab_name)
        return continue_test

